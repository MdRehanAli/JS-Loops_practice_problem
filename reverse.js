var num = 0;
while (num <= 10) {
    console.log(num);
    num++;
}

// while loop reverse way 
var num = 10;
while (num >= 0) {
    console.log(num);
    num--;
}

for (var i = 0; i <= 10; i++) {
    console.log(i);
}

// For loop in reverse way 

for ( var i = 10; i >= 1; i--){
    console.log(i);
}